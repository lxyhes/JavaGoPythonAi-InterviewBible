export const useI18nStore = defineStore('i18n', () => {
  // State
  const locale = ref<Locale>(localStorage.getItem('locale') as Locale || 'zh')

  // Getters
  const messages = computed(() => (locale.value === 'zh' ? zhMessages : enMessages))

  // Actions
  function setLocale(newLocale: Locale) {
    locale.value = newLocale
    localStorage.setItem('locale', newLocale)
    document.documentElement.setAttribute('lang', newLocale)
  }

  function t(key: string, params?: Record<string, string | number>): string {
    const keys = key.split('.')
    let value: unknown = messages.value

    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = (value as Messages)[k]
      } else {
        return key
      }
    }

    if (typeof value !== 'string') {
      return key
    }

    if (params) {
      return value.replace(/\{(\w+)\}/g, (_, paramKey) => {
        const paramValue = params[paramKey]
        return paramValue !== undefined ? String(paramValue) : `{${paramKey}}`
      })
    }

    return value
  }

  function initLocale() {
    document.documentElement.setAttribute('lang', locale.value)
  }

  return {
    locale,
    messages,
    setLocale,
    t,
    initLocale,
  }
})
